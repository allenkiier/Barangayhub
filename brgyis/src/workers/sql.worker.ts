      // Precomputed hash/salt for password 'admin123' using PBKDF2, 150000 iterations, 16-byte salt, SHA-256
      // Generated by generate-admin-hash.js
      const salt = 'a4zfH3hpiF58dOUhMUMAAw==';
      const hash = 'M0iq+elJ7kD1Sk2ExAN/Yhnyg1XM0fwzz8K186nPysc=';
      const now = new Date().toISOString();
